class _BarItem {
  final String label;
  final double value;
  final String unit;
  const _BarItem(
      {required this.label, required this.value, required this.unit});
}

class _OperationsBarChart extends StatelessWidget {
  final List<_BarItem> items;
  final Color barColor;
  const _OperationsBarChart(
      {required this.items, required this.barColor});

  @override
  Widget build(BuildContext context) {
    if (items.isEmpty) return const SizedBox.shrink();
    final maxValue =
        items.map((i) => i.value).reduce((a, b) => a > b ? a : b);
    final safeMax = maxValue > 0 ? maxValue : 1.0;

    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: items.map((item) {
        final ratio = item.value / safeMax;
        return Padding(
          padding: const EdgeInsets.only(bottom: 8),
          child: Row(
            children: [
              SizedBox(
                width: 90,
                child: Text(
                  item.label,
                  style: TextStyle(
                      fontSize: 11, color: AppColors.textHint),
                  overflow: TextOverflow.ellipsis,
                  maxLines: 1,
                ),
              ),
              const SizedBox(width: 8),
              Expanded(
                child: ClipRRect(
                  borderRadius: BorderRadius.circular(3),
                  child: LinearProgressIndicator(
                    value: ratio,
                    minHeight: 12,
                    backgroundColor:
                        barColor.withValues(alpha: 0.1),
                    valueColor:
                        AlwaysStoppedAnimation<Color>(barColor),
                  ),
                ),
              ),
              const SizedBox(width: 8),
              Text(
                item.value > 0
                    ? '${item.value.toStringAsFixed(item.value == item.value.roundToDouble() ? 0 : 1)} ${item.unit}'
                    : '-',
                style: TextStyle(
                  fontSize: 11,
                  fontWeight: FontWeight.w600,
                  color: barColor,
                ),
              ),
            ],
          ),
        );
      }).toList(),
    );
  }
}

