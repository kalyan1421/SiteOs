class ReportsScreen extends ConsumerStatefulWidget {
  final String? projectId;

  const ReportsScreen({super.key, this.projectId});

  @override
  ConsumerState<ReportsScreen> createState() => _ReportsScreenState();
}

class _ReportsScreenState extends ConsumerState<ReportsScreen> {
  bool _isExporting = false;

  @override
  void initState() {
    super.initState();
    WidgetsBinding.instance.addPostFrameCallback((_) {
      ref
          .read(reportProvider.notifier)
          .loadReports(projectId: widget.projectId);
    });
  }

  Future<void> _exportPdf(ReportState state) async {
    if (_isExporting) return;
    setState(() => _isExporting = true);
    try {
      final doc = pw.Document();
      final stats = state.stats;
      final generatedAt = DateTime.now();

      doc.addPage(
        pw.MultiPage(
          pageFormat: PdfPageFormat.a4,
          margin: const pw.EdgeInsets.all(32),
          build: (pw.Context ctx) => [
            pw.Row(
              mainAxisAlignment: pw.MainAxisAlignment.spaceBetween,
              children: [
                pw.Text(
                  'Project Insights Report',
                  style: pw.TextStyle(
                      fontSize: 22, fontWeight: pw.FontWeight.bold),
                ),
                pw.Text(
                  '${generatedAt.day}/${generatedAt.month}/${generatedAt.year}',
                  style: const pw.TextStyle(
                      fontSize: 11, color: PdfColors.grey600),
                ),
              ],
            ),
            if (widget.projectId != null)
              pw.Text(
                'Project ID: ${widget.projectId}',
                style: const pw.TextStyle(
                    fontSize: 10, color: PdfColors.grey500),
              ),
            pw.SizedBox(height: 16),
            pw.Divider(),
            pw.SizedBox(height: 12),
            pw.Text('Financial Summary',
                style: pw.TextStyle(
                    fontSize: 16, fontWeight: pw.FontWeight.bold)),
            pw.SizedBox(height: 8),
            _pdfRow('Total Expenses',
                '₹${stats.totalExpenses.toStringAsFixed(2)}'),
            _pdfRow(
                'Labour Cost', '₹${stats.laborCost.toStringAsFixed(2)}'),
            _pdfRow('Material Cost',
                '₹${stats.materialCost.toStringAsFixed(2)}'),
            _pdfRow('Machinery Cost',
                '₹${stats.machineryCost.toStringAsFixed(2)}'),
            _pdfRow(
                'Other Cost', '₹${stats.otherCost.toStringAsFixed(2)}'),
            pw.SizedBox(height: 16),
            if (stats.chartData.isNotEmpty) ...[
              pw.Text('Monthly Breakdown',
                  style: pw.TextStyle(
                      fontSize: 16, fontWeight: pw.FontWeight.bold)),
              pw.SizedBox(height: 8),
              pw.TableHelper.fromTextArray(
                headers: ['Period', 'Amount (₹)'],
                data: stats.chartData
                    .map((d) => [d.label, d.value.toStringAsFixed(2)])
                    .toList(),
                headerStyle: pw.TextStyle(fontWeight: pw.FontWeight.bold),
                cellAlignment: pw.Alignment.centerLeft,
                headerDecoration:
                    const pw.BoxDecoration(color: PdfColors.blueGrey100),
              ),
              pw.SizedBox(height: 16),
            ],
            pw.Divider(),
            pw.SizedBox(height: 8),
            pw.Text(
              'Generated by SiteOS · ${generatedAt.toIso8601String()}',
              style: const pw.TextStyle(
                  fontSize: 9, color: PdfColors.grey500),
            ),
          ],
        ),
      );

      await Printing.layoutPdf(
        onLayout: (_) async => doc.save(),
        name:
            'report_${widget.projectId ?? "all"}_${generatedAt.millisecondsSinceEpoch}.pdf',
      );
    } catch (e) {
      logger.e('PDF export failed: $e');
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
              content: Text('Export failed: $e'),
              backgroundColor: AppColors.error),
        );
      }
    } finally {
      if (mounted) setState(() => _isExporting = false);
    }
  }

  pw.Widget _pdfRow(String label, String value) {
    return pw.Padding(
      padding: const pw.EdgeInsets.symmetric(vertical: 3),
      child: pw.Row(
        mainAxisAlignment: pw.MainAxisAlignment.spaceBetween,
        children: [
          pw.Text(label, style: const pw.TextStyle(fontSize: 11)),
          pw.Text(value,
              style: pw.TextStyle(
                  fontSize: 11, fontWeight: pw.FontWeight.bold)),
        ],
      ),
    );
  }

  @override
  Widget build(BuildContext context) {
    final state = ref.watch(reportProvider);
    final notifier = ref.read(reportProvider.notifier);
    final suppliersAsync = ref.watch(suppliersProvider);

    return Scaffold(
      backgroundColor: AppColors.scaffoldBackground,
      body: LayoutBuilder(
        builder: (context, constraints) {
          final r = R(Size(constraints.maxWidth, constraints.maxHeight));

          return SafeArea(
            child: RefreshIndicator(
              onRefresh: () =>
                  notifier.refresh(projectId: widget.projectId),
              child: SingleChildScrollView(
                padding: EdgeInsets.fromLTRB(
                  r.isDesktop ? 32 : 20,
                  r.isDesktop ? 28 : 8,
                  r.isDesktop ? 32 : 20,
                  r.isDesktop ? 28 : 16,
                ),
                child: Align(
                  alignment: Alignment.topCenter,
                  child: ConstrainedBox(
                    constraints:
                        BoxConstraints(maxWidth: r.maxContentWidth),
                    child: Column(
                      crossAxisAlignment: CrossAxisAlignment.start,
                      children: [
                        // Header
                        _buildHeader(context, state),
                        const SizedBox(height: 20),

                        // Filters
                        _buildFilters(context, state, notifier,
                            suppliersAsync),
                        const SizedBox(height: 24),

                        if (state.isLoading &&
                            state.stats == FinancialStats.empty)
                          const LoadingWidget(
                              message: 'Loading insights...')
                        else if (state.error != null &&
                            state.stats == FinancialStats.empty)
                          AppErrorWidget(
                            message: state.error!,
                            onRetry: () => notifier.refresh(),
                          )
                        else ...[
                          // KPI summary row
                          _KpiSummaryRow(stats: state.stats),
                          const SizedBox(height: 20),

                          // Desktop: side-by-side charts
                          if (r.isDesktop)
                            Row(
                              crossAxisAlignment:
                                  CrossAxisAlignment.start,
                              children: [
                                Expanded(
                                    flex: 3,
                                    child: _FinancialHealthCard(
                                        stats: state.stats)),
                                const SizedBox(width: 20),
                                Expanded(
                                    flex: 2,
                                    child: _ResourceSplitCard(
                                        stats: state.stats)),
                              ],
                            )
                          else ...[
                            _FinancialHealthCard(stats: state.stats),
                            const SizedBox(height: 16),
                            _ResourceSplitCard(stats: state.stats),
                          ],
                          const SizedBox(height: 20),

                          // Project-wise breakdown (only on global reports)
                          if (widget.projectId == null) ...[
                            _ProjectBreakdownSection(
                              materialRows: state.materialVendors,
                              machineryRows: state.machineryProjects,
                              labourRows: state.labourProjects,
                              isLoading: state.isLoading,
                            ),
                            const SizedBox(height: 20),
                            _VendorAnalyticsSection(),
                            const SizedBox(height: 20),
                          ],

                          _OperationsReports(
                            materialRows: state.materialVendors,
                            machineryRows: state.machineryProjects,
                            labourRows: state.labourProjects,
                            isLoading: state.isLoading,
                          ),
                          const SizedBox(height: 20),

                          if (state.stats.growthPercentage != 0)
                            _InsightTip(
                                growth: state.stats.growthPercentage),

                          const SizedBox(height: 40),
                        ],
                      ],
                    ),
                  ),
                ),
              ),
            ),
          );
        },
      ),
    );
  }

  Widget _buildHeader(BuildContext context, ReportState state) {
    return Row(
      crossAxisAlignment: CrossAxisAlignment.end,
      children: [
        Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            Text(
              'ANALYTICS',
              style: GoogleFonts.plusJakartaSans(
                fontSize: 11,
                fontWeight: FontWeight.w700,
                color: AppColors.secondaryDark,
                letterSpacing: 2.2,
              ),
            ),
            const SizedBox(height: 6),
            Text(
              'Reports',
              style: GoogleFonts.fraunces(
                fontSize: 32,
                fontWeight: FontWeight.w400,
                color: AppColors.textPrimary,
                letterSpacing: -1.0,
                height: 1.0,
              ),
            ),
            if (widget.projectId != null) ...[
              const SizedBox(height: 6),
              Container(
                padding: const EdgeInsets.symmetric(horizontal: 10, vertical: 4),
                decoration: BoxDecoration(
                  color: AppColors.primary.withValues(alpha: 0.1),
                  borderRadius: BorderRadius.circular(8),
                ),
                child: Text(
                  'Project',
                  style: TextStyle(
                    fontSize: 11,
                    fontWeight: FontWeight.w600,
                    color: AppColors.primary,
                  ),
                ),
              ),
            ],
          ],
        ),
        const Spacer(),
        Material(
          color: Colors.white,
          borderRadius: BorderRadius.circular(10),
          child: InkWell(
            onTap: _isExporting ? null : () => _exportPdf(state),
            borderRadius: BorderRadius.circular(10),
            child: Container(
              height: 40,
              padding: const EdgeInsets.symmetric(horizontal: 14),
              decoration: BoxDecoration(
                borderRadius: BorderRadius.circular(10),
                border: Border.all(color: AppColors.border),
              ),
              child: Row(
                mainAxisSize: MainAxisSize.min,
                children: [
                  if (_isExporting)
                    const SizedBox(
                      width: 16,
                      height: 16,
                      child: CircularProgressIndicator(strokeWidth: 2),
                    )
                  else
                    Icon(Icons.download_rounded,
                        size: 17, color: AppColors.textPrimary),
                  const SizedBox(width: 6),
                  Text(
                    'Export',
                    style: TextStyle(
                      fontSize: 13,
                      fontWeight: FontWeight.w600,
                      color: AppColors.textPrimary,
                    ),
                  ),
                ],
              ),
            ),
          ),
        ),
      ],
    );
  }

  Widget _buildFilters(
    BuildContext context,
    ReportState state,
    ReportNotifier notifier,
    AsyncValue<List<SupplierModel>> suppliersAsync,
  ) {
    return Column(
      children: [
        // Time period tabs
        Container(
          height: 42,
          decoration: BoxDecoration(
            color: AppColors.surfaceVariant,
            borderRadius: BorderRadius.circular(10),
          ),
          padding: const EdgeInsets.all(3),
          child: Row(
            children: TimePeriod.values.map((period) {
              final isSelected = state.selectedPeriod == period;
              return Expanded(
                child: GestureDetector(
                  onTap: () => notifier.setPeriod(period,
                      projectId: widget.projectId),
                  child: AnimatedContainer(
                    duration: const Duration(milliseconds: 150),
                    alignment: Alignment.center,
                    decoration: BoxDecoration(
                      color:
                          isSelected ? Colors.white : Colors.transparent,
                      borderRadius: BorderRadius.circular(8),
                      boxShadow: isSelected
                          ? const [
                              BoxShadow(
                                color: Color(0x0A000000),
                                blurRadius: 4,
                                offset: Offset(0, 1),
                              ),
                            ]
                          : null,
                    ),
                    child: Text(
                      period.label,
                      style: TextStyle(
                        fontWeight: isSelected
                            ? FontWeight.w600
                            : FontWeight.w500,
                        color: isSelected
                            ? AppColors.textPrimary
                            : AppColors.textSecondary,
                        fontSize: 13,
                      ),
                    ),
                  ),
                ),
              );
            }).toList(),
          ),
        ),
        const SizedBox(height: 12),

        // Vendor filter
        suppliersAsync.when(
          data: (suppliers) => _VendorFilter(
            suppliers: suppliers,
            selectedVendorId: state.selectedVendorId,
            onChanged: (vendorId) =>
                notifier.setVendor(vendorId, projectId: widget.projectId),
          ),
          loading: () => const SizedBox.shrink(),
          error: (_, _) => const SizedBox.shrink(),
        ),
      ],
    );
  }
}

// ── KPI Summary Row ──

class _KpiSummaryRow extends StatelessWidget {
  final FinancialStats stats;
  const _KpiSummaryRow({required this.stats});

  @override
  Widget build(BuildContext context) {
    final items = [
      _KpiItem(
        label: 'Total Spend',
        value: _fmt(stats.totalExpenses),
        icon: Icons.currency_rupee_rounded,
        color: AppColors.primary,
      ),
      _KpiItem(
        label: 'Labour',
        value: _fmt(stats.laborCost),
        icon: Icons.people_outline_rounded,
        color: AppColors.warning,
      ),
      _KpiItem(
        label: 'Material',
        value: _fmt(stats.materialCost),
        icon: Icons.inventory_2_outlined,
        color: AppColors.success,
      ),
      _KpiItem(
        label: 'Machinery',
        value: _fmt(stats.machineryCost),
        icon: Icons.precision_manufacturing_outlined,
        color: AppColors.info,
      ),
    ];

    return GridView.count(
      crossAxisCount: 2,
      shrinkWrap: true,
      physics: const NeverScrollableScrollPhysics(),
      crossAxisSpacing: 12,
      mainAxisSpacing: 12,
      childAspectRatio: 2.2,
      children: items.map((item) => _KpiCard(item: item)).toList(),
    );
  }

  String _fmt(double v) => v >= 100000
      ? '₹${(v / 100000).toStringAsFixed(1)}L'
      : v >= 1000
          ? '₹${(v / 1000).toStringAsFixed(0)}K'
          : '₹${v.toStringAsFixed(0)}';
}

